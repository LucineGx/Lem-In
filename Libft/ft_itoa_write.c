#include "libft.h"

void		ft_itoa_write(int n)
{
	char c;

	if (n == -0)
		write(1, "0", 1);
	else if (n < 0)
	{
		write(1, "-", 1);
		if (n == -2147483648)
		{
			write(1, "2", 1);
			ft_itoa_write(147483648);
		}
		else
			ft_itoa_write(-n);
	}
	else
	{
		c = (n % 10) + 48;
		write(1, &c, 1);
		if (n > 9)
			ft_itoa_write(n / 10);
	}
}
